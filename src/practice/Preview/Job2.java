/**
 * Auto plus.
 */
public class Job2 {
    public static void main(String[] args) {
        int i = 0, j = 0;
        System.out.println(i++ + j++);

        int a = 0, b = 0;
        System.out.println(a++ + ++b);

        int c = 0, d = 0;
        System.out.println(c-- + ++d);

        int e = 0, f = 0;
        System.out.println(--e + ++f);
    }
}
